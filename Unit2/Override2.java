package Class;
public class in1 extends spur
{
    int twice(int num);
    {
        return num*2;
    }
}
public static void main(String[] args)
{
    spur ord = new spur();
    System.out.println(ord.twice(3));
    in1 ord1 = new in1();
    System.out.println(int1.twice(3));
    ord = ord1;
    System.out.println(ord.twice(3));
}
